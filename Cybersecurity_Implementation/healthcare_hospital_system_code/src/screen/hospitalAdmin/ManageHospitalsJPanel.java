    private void populateTableAccordingToSelectedHospital() {
        DefaultTableModel dtm = (DefaultTableModel) tblHospitalDepartments.getModel();
        dtm.setRowCount(0);
        if (selectedHospitalBranch != null) {
            for (Department depart : selectedHospitalBranch.getDepartmentDirectory().getDepartmentList()) {
                for (DepartmentType dep : Department.DepartmentType.values()) {
                    if (dep.getValue().equals(depart.getName())) {
                        Object[] row = new Object[2];
                        row[0] = depart;
                        row[1] = depart.getDepartmentID();
                        dtm.addRow(row);
                    }
                }
            }
        }
    }
    
    private void cmbDepartmentsActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cmbDepartmentsActionPerformed
        // TODO add your handling code here:
        if (cmbDepartments.getItemCount() != 0) {
            if (cmbDepartments.getSelectedItem().equals("Select a department")) {
                btnAddDepartment.setVisible(false);
                selectedDepartment = null;
            } else {
                btnAddDepartment.setVisible(true);
                selectedDepartment = (DepartmentType) cmbDepartments.getSelectedItem();
            }
        }
    }//GEN-LAST:event_cmbDepartmentsActionPerformed

    private void btnAddDepartmentActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddDepartmentActionPerformed
        // TODO add your handling code here:
        if(selectedDepartment!=null && selectedHospitalBranch!=null){
            department=selectedHospitalBranch.getDepartmentDirectory().createDepartment(selectedDepartment);
            System.out.println(selectedHospitalBranch.getHospitalBranchName());
            log.info(UtilFunctions.encrypt("ADD_DEP : "+account.getEmployee()));
            System.out.println(department.getDepartmentID()+" "+department.getName());
            System.out.println("Total departments --> "+selectedHospitalBranch.getDepartmentDirectory().getDepartmentList().size());
            populateTableAccordingToSelectedHospital();
            populateDepartments();
        }
    }//GEN-LAST:event_btnAddDepartmentActionPerformed

    private void btnDeleteDepartmentActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteDepartmentActionPerformed
        // TODO add your handling code here:
        int selectedRow = tblHospitalDepartments.getSelectedRow();
        if(selectedRow<0){
            JOptionPane.showMessageDialog(null, "Please select a department to delete", "WARNING", JOptionPane.WARNING_MESSAGE);
        }
        else{
            Department dept = (Department) tblHospitalDepartments.getValueAt(selectedRow, 0);
            log.info(UtilFunctions.encrypt("DEL_DEP : "+account.getEmployee()));
            selectedHospitalBranch.getDepartmentDirectory().deleteDepartment(dept);
            populateTableAccordingToSelectedHospital();
            populateDepartments();
        }
    }//GEN-LAST:event_btnDeleteDepartmentActionPerformed


