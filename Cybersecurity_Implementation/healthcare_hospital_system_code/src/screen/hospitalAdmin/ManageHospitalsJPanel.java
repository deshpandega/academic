    private void populateDepartments() {
        cmbDepartments.removeAllItems();
        cmbDepartments.addItem("Select a department");
        if (selectedHospitalBranch!=null&&(!selectedHospitalBranch.getDepartmentDirectory().getDepartmentList().isEmpty())) {
            ArrayList<DepartmentType> departmentList = new ArrayList<>();
            for (DepartmentType dep : Department.DepartmentType.values()) {
                int i=0;
                for (Department depart : selectedHospitalBranch.getDepartmentDirectory().getDepartmentList()) {
                    if (dep.getValue().equals(depart.getName())) {
                        i++;
                        break;
                    }
                }
                if(i==0){
                    cmbDepartments.addItem(dep);
                }
            }
        } else {
            for (Department.DepartmentType department : Department.DepartmentType.values()) {
                cmbDepartments.addItem(department);
            }
        }
    }
    
