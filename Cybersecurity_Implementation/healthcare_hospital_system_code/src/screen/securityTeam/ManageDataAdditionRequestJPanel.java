    void populateRequestsTable(){
        DefaultTableModel dtm = (DefaultTableModel) tblSpecialRequests.getModel();
        dtm.setRowCount(0);
        for (WorkRequest request : securityDepartment.getWorkQueue().getWorkRequestList()) {
            if (request instanceof MedicalDataAdditionWorkRequest) {
                Object[] row = new Object[6];
                row[0] = request;
                row[1] = request.getRequestCreator();
                row[2] = request.getRequestAssignee() == null ? null : request.getRequestAssignee();
                row[3] = request.getRequestCreationDate();
                row[4] = request.getStatus();
                row[5] = request.getRequestOutcome();
                dtm.addRow(row);
            }
        }
    }
    
