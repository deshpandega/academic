    private void btnAssignRequestActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAssignRequestActionPerformed
        // TODO add your handling code here:
        int selectedRequestRow = tblSpecialRequests.getSelectedRow();
        if(selectedRequestRow<0){
            JOptionPane.showMessageDialog(null, "Please seect a request to assign", "WARNING", JOptionPane.WARNING_MESSAGE);
            return;
        }

        WorkRequest request = (WorkRequest) tblSpecialRequests.getValueAt(selectedRequestRow, 0);
        if(request.getStatus().equals("Initiated")){
            request.setRequestAssignee(account.getEmployee());
            request.setStatus("Pending");
            populateRequestsTable();
        }
        else if(request.getRequestAssignee().equals(account.getEmployee())){
            JOptionPane.showMessageDialog(null, "Request already assigned to you", "WARNING", JOptionPane.WARNING_MESSAGE);
            return;
        }
        else{
            JOptionPane.showMessageDialog(null, "Request already being processed by someone else", "WARNING", JOptionPane.WARNING_MESSAGE);
            return;
        }
    }//GEN-LAST:event_btnAssignRequestActionPerformed

    private void btnProcessRequestActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnProcessRequestActionPerformed
        // TODO add your handling code here:
        int selectedRequestRow = tblSpecialRequests.getSelectedRow();
        if(selectedRequestRow<0){
            JOptionPane.showMessageDialog(null, "Please select a request to process", "WARNING", JOptionPane.WARNING_MESSAGE);
            return;
        }

        MedicalDataAdditionWorkRequest request = (MedicalDataAdditionWorkRequest) tblSpecialRequests.getValueAt(selectedRequestRow, 0);
        if(null==request.getRequestAssignee()){
            JOptionPane.showMessageDialog(null, "Please assign the request first, to process", "WARNING", JOptionPane.WARNING_MESSAGE);
            return;
        }
        if(request.getRequestAssignee().equals(account.getEmployee())){
            if(request.getStatus().equalsIgnoreCase("Initiated")){
                JOptionPane.showMessageDialog(null, "Please assign the request before processing", "WARNING", JOptionPane.WARNING_MESSAGE);
                return;
            }
            if(!request.getStatus().equalsIgnoreCase("Complete")){
                request.setStatus("Processing");
            }
            
            ProcessMedicalDataAdditionRequestJPanel processMedicalDataAdditionRequestJPanel = new ProcessMedicalDataAdditionRequestJPanel(cardContainer, account, request);
            cardContainer.add("processMedicalDataAdditionRequestJPanel", processMedicalDataAdditionRequestJPanel);
            CardLayout layout = (CardLayout) cardContainer.getLayout();
            layout.next(cardContainer);
        }
        else{
            JOptionPane.showMessageDialog(null, "Request is not assigned to you", "WARNING", JOptionPane.WARNING_MESSAGE);
            return;
        }
    }//GEN-LAST:event_btnProcessRequestActionPerformed

    private void goBack(){
        cardContainer.remove(this);
        CardLayout layout = (CardLayout) cardContainer.getLayout();
        layout.previous(cardContainer);
    }

